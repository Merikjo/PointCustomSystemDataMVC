    CONSTRAINT [FK_Reservation_ToTable_5] FOREIGN KEY ([User_id]) REFERENCES [dbo].[User] ([User_id]),
    CONSTRAINT [FK_Reservation_ToTable_2] FOREIGN KEY ([Customer_id]) REFERENCES [dbo].[Customer] ([Customer_id]),
    CONSTRAINT [FK_Reservation_ToTable_6] FOREIGN KEY ([TreatmentOffice_id]) REFERENCES [dbo].[TreatmentOffice] ([TreatmentOffice_id])
);

